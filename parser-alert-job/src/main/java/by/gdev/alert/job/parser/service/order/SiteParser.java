package by.gdev.alert.job.parser.service.order;

import by.gdev.common.model.OrderDTO;
import by.gdev.common.model.SiteName;

import java.util.List;

/**
 * Базовый интерфейс для всех парсеров сайтов‑источников.
 * Определяет минимальный контракт, который должна реализовать каждая конкретная реализация парсера:
 *  - запуск процесса парсинга;
 *  - идентификация сайта‑источника;
 *  - проверка активности парсера.
 */
public interface SiteParser {

    /**
     * Запускает процесс парсинга для конкретного сайта.
     * Реализация должна вернуть список заказов в формате {@link OrderDTO}.
     *
     * @return список заказов, полученных с сайта
     */
    List<OrderDTO> parse();

    /**
     * Возвращает идентификатор сайта‑источника.
     * Используется для определения, к какому сайту относится данный парсер.
     *
     * @return значение из {@link SiteName}
     */
    SiteName getSiteName();

    /**
     * Проверяет, активен ли парсер.
     * Если парсер отключён (например, через конфигурацию), метод должен вернуть false.
     *
     * @return true, если парсер активен и должен выполняться
     */
    boolean isActive();
}