package by.gdev.alert.job.llm.domain.promt;

/**
 * Типы AI‑промтов, определяющие их назначение и область применения.
 * <p>
 * Используются для классификации промтов в системе автоответов
 * и выбора корректного шаблона при обработке заказов.
 * <p>
 * Значения:
 * <ul>
 *     <li>{@link #DEFAULT} — базовый универсальный промт.</li>
 *     <li>{@link #DESIGN} — промты, связанные с дизайном, UI/UX, графикой.</li>
 *     <li>{@link #DEVELOPMENT} — промты для разработки, программирования, технических задач.</li>
 *     <li>{@link #CHATBOTS} — промты для генерации логики чат‑ботов и диалогов.</li>
 * </ul>
 */
public enum AiPromptType {

    /** Универсальный промт по умолчанию */
    DEFAULT,

    /** Промты, связанные с дизайном, UI/UX и графикой */
    DESIGN,

    /** Промты для задач разработки и программирования */
    DEVELOPMENT,

    /** Промты для генерации логики чат‑ботов */
    CHATBOTS
}
